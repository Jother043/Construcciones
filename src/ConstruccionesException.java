public class ConstruccionesException extends Exception {
    public ConstruccionesException() {
    }

    public ConstruccionesException(String message) {
        super(message);
    }

    public ConstruccionesException(String message, Throwable cause) {
        super(message, cause);
    }

    public ConstruccionesException(Throwable cause) {
        super(cause);
    }

    public ConstruccionesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
